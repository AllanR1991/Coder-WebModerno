// Quando o valor é inputado diretamente é conhecido como valor literal

console.log("Usando valores literais: ")
console.log("Caneta");
console.log(10);
console.log(6.4);
console.log(1.5);
console.log(7.9);

// Usando variavel no lugar de valor literal
console.log("\n\nUsando valores variaveis: ")
var nome = "Caneta";
var quantidade = 10;
var preco = 6.4;
var imposto = 1.5;
var precoFinal = preco + imposto;

console.log(nome);
console.log(quantidade);
console.log(preco);
console.log(imposto);
console.log(precoFinal);
